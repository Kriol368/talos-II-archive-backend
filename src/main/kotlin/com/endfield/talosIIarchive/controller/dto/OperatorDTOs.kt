    basicAttackDescription = this.basicAttackDescription,
    battleSkill = this.battleSkill,
    battleSkillDescription = this.battleSkillDescription,
    battleSkillSpCost = this.battleSkillSpCost,
    comboSkill = this.comboSkill,
    comboSkillDescription = this.comboSkillDescription,
    comboSkillCooldown = this.comboSkillCooldown,
    ultimate = this.ultimate,
    ultimateDescription = this.ultimateDescription,
    ultimateEnergyCost = this.ultimateEnergyCost,
    baseTalent1 = this.baseTalent1,
    baseTalent2 = this.baseTalent2,
    combatTalent1 = this.combatTalent1,
    combatTalent2 = this.combatTalent2,
    p1 = this.p1,
    p1Effect = this.p1Effect,
