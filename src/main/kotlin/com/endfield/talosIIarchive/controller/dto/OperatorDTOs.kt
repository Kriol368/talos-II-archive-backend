package com.endfield.talosIIarchive.controller.dto

import com.endfield.talosIIarchive.model.Operator

data class OperatorSummaryDTO(
    val id: Long,
    val name: String,
    val rarity: String,
    val imageUrl: String?,
    val element: String,
    val weaponType: String,
    val operatorClass: String
)

data class OperatorDetailDTO(
    val id: Long,
    val name: String,
    val rarity: String,
    val imageUrl: String?,
    val element: String,
    val weaponType: String,
    val operatorClass: String,
    val strength: Int,
    val agility: Int,
    val intellect: Int,
    val will: Int,
    val basicAttack: String,
    val basicAttackDescription: String,
    val battleSkill: String,
    val battleSkillDescription: String,
    val battleSkillSpCost: Int,
    val comboSkill: String,
    val comboSkillDescription: String,
    val comboSkillCooldown: Int,
    val ultimate: String,
    val ultimateDescription: String,
    val ultimateEnergyCost: Int,
    val baseTalent1: String,
    val baseTalent2: String,
    val combatTalent1: String,
    val combatTalent2: String,
    val p1: String?,
    val p1Effect: String?,
    val p2: String?,
    val p2Effect: String?,
    val p3: String?,
    val p3Effect: String?,
    val p4: String?,
    val p4Effect: String?,
    val p5: String?,
    val p5Effect: String?,
    val conceptArt1: String?,
    val conceptArt2: String?,
    val conceptArt3: String?
)

fun Operator.toSummaryDTO() = OperatorSummaryDTO(
    id = this.id,
    name = this.name,
    rarity = "${this.rarity.stars}★",
    imageUrl = this.imageUrl,
    element = this.element.name,
    weaponType = this.weaponType.name,
    operatorClass = this.operatorClass.name
)

fun Operator.toDetailDTO() = OperatorDetailDTO(
    id = this.id,
    name = this.name,
    rarity = "${this.rarity.stars}★",
    imageUrl = this.imageUrl,
    element = this.element.name,
    weaponType = this.weaponType.name,
    operatorClass = this.operatorClass.name,
    strength = this.strength,
    agility = this.agility,
    intellect = this.intellect,
    will = this.will,
